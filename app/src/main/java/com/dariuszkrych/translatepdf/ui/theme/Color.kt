val White = Color(0xFFFFFFFF)       // Pure white. Light mode background, dark mode foreground.
val DarkGrey = Color(0xFF333333)    // Neutral dark grey used for containers and toolbars in dark mode.
val LightGrey = Color(0xFFDDDDDD)   // Neutral light grey used for containers and toolbars in light mode.
